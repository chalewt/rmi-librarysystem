    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed
        boolean rmiRegStarted = false;
        try {
            rmiRegStarted = startRegistry();
        } catch (RemoteException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        if (rmiRegStarted) {
            txtServerPort.setEditable(false);
            txtService.setText("DistributedLibrary");
            txtServerStatus.setText("Running . . .");
            btnStart.setVisible(false);
            btnUpdate.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Failed to start the registry.", "Distributed Library System", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnStartActionPerformed

