    private void getDbServerInfo() {
        txtDbServer.setText(dbServerName);
        txtDbServer.setEditable(false);
        txtDatabase.setText(dbName);
        txtDatabase.setEditable(false);
        txtUserName.setText(userName);
        txtUserName.setEditable(false);
        txtPassword.setText(password);
        txtPassword.setEditable(false);
        btnUpdate.setText("Edit");
    }
    //////********** Create and Start RMI Registry ****************************//
    public boolean startRegistry() throws RemoteException {
        // create in server registry        
        boolean f;
        PORT = Integer.parseInt(txtServerPort.getText());
        try {
            registry = LocateRegistry.createRegistry(PORT);
            //register the remote object
            registry.rebind("DistributedLibrary", new DistributedLibraryImpl()); 
            f=true;            
        } catch (RemoteException ex) {                   
           f = false;           
        }
        return f;
    }
