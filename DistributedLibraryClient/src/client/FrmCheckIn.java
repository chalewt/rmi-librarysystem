    private void ppmiCheckInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ppmiCheckInActionPerformed
        String[] chkInfo = new String[3]; 
        DateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date date = new Date();
        chkInfo[0]= dateFormat.format(date);
        chkInfo[1] = this.txtCardNumber.getText();
        int row = this.tblCheckOutDetatil.getSelectedRow();
        Object str = this.tblCheckOutDetatil.getValueAt(row, 3);
        chkInfo[2] = str.toString();
        checkIn(chkInfo);
    }//GEN-LAST:event_ppmiCheckInActionPerformed

