    private void patronsearch(String cNum) {              
        try {
            Object[][] getPat;
            ClientServerInfo lookup = new ClientServerInfo();
            DistributedLibraryInterface viewPatron;
            viewPatron = lookup.getLookup();
            if (viewPatron == null) {
                throw new java.net.ConnectException();
            }
            getPat = viewPatron.getPatron(cNum);            
            if (getPat.length == 0) {
                JOptionPane.showMessageDialog(this, "No record found with card number : " + this.txtCardNumber.getText());
                this.txtCardNumber.requestFocus();
            } else {
                this.txtFullName.setText(getPat[0][1].toString()+" "+ getPat[0][2].toString());    
                getCheckOut(this.txtCardNumber.getText());
            }
        } catch (java.net.ConnectException conex) {
            JOptionPane.showMessageDialog(this, "Connection refused to Server : " + ClientServerInfo.serverName + "\n Check your network connection or \n Contact your Network Administrator.", "Distributed Library System", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (HeadlessException | RemoteException ex) {
            JOptionPane.showMessageDialog(this, ex.toString(), "Distributed Library System", JOptionPane.ERROR_MESSAGE);
        }
    }
    private void getCheckOut(String cNum) {
        String[] str = new String[]{"ISBN", "Title", "Author","Accession number", "Check out date", "Check in date"};
        try {           
            Object[][] checkInfo;
            ClientServerInfo lookup = new ClientServerInfo();
            DistributedLibraryInterface checkOutDetail;
            checkOutDetail = lookup.getLookup();
            if (checkOutDetail == null) {
                throw new java.net.ConnectException();
            }
            checkInfo = checkOutDetail.getCheckOutDetail(cNum);
             if (checkInfo.length == 0) {
                JOptionPane.showMessageDialog(this, "No record found with Card Number : "+cNum);               
                this.txtCardNumber.requestFocus();
             }              
             this.tblCheckOutDetatil.setModel(new javax.swing.table.DefaultTableModel(checkInfo, str));                
            
        } catch (java.net.ConnectException conex) {
            JOptionPane.showMessageDialog(this, "Connection refused to Server : " + ClientServerInfo.serverName + "\n Check your network connection or \n Contact your Network Administrator.", "Distributed Library System", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (HeadlessException | RemoteException ex) {
            JOptionPane.showMessageDialog(this, ex.toString(), "Distributed Library System", JOptionPane.ERROR_MESSAGE);
        }
    }
    private void checkIn(String[] chkInfo ) {
        boolean saved;
        ClientServerInfo lookup = new ClientServerInfo();
        DistributedLibraryInterface addCheckIn;
        try {
            addCheckIn = lookup.getLookup();
            if (addCheckIn == null) {
                throw new java.net.ConnectException();
            }
            saved = addCheckIn.checkIn(chkInfo);
            if (saved) {
                JOptionPane.showMessageDialog(this, "Check in successfully completed to : " + this.txtFullName.getText() + "\nItem Accession number : " + chkInfo[2], "Distributed Library System", JOptionPane.INFORMATION_MESSAGE);
                patronsearch(this.txtCardNumber.getText());
            } else {
                JOptionPane.showMessageDialog(this, "Oops! check in faild.");
                // this.dispose();
            }
        } catch (java.net.ConnectException conex) {
            JOptionPane.showMessageDialog(this, "Connection refused to Server : " + ClientServerInfo.serverName + "\n Check your network connection or \n Contact your Network Administrator.", "Distributed Library System", JOptionPane.INFORMATION_MESSAGE);
        } catch (HeadlessException | RemoteException ex) {
            JOptionPane.showMessageDialog(this, ex.toString(), "Distributed Library System", JOptionPane.ERROR_MESSAGE);
        }
    }
